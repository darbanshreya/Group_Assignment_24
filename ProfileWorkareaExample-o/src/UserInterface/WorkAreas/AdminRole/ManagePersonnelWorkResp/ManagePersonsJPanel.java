    private boolean validatePerson(String email) {

        PersonDirectory persondirectory = business.getPersonDirectory();

        if (persondirectory.findPersonByEmailId(email) == null) {
            return true;
        }

        clearFields();
        JOptionPane.showMessageDialog(this, "Account already exist please use different email id: ", "Error",
                JOptionPane.INFORMATION_MESSAGE);
        return false;
    }

