    private void initializeCombo() {
        cmbDepartment.removeAllItems();
        cmbPersonType1.removeAllItems();

        for (String type : GlobalVariables.PERSONA_TYPE) {
            cmbPersonType1.addItem(type);
        }

        for (Department dept : business.getDepartmentDictonary().getDepartments()) {
            cmbDepartment.addItem(dept.getName());
        }
    }

