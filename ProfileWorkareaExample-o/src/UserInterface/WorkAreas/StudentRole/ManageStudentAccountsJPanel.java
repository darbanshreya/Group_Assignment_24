    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblAccounts.getSelectedRow();

        if (selectedRow >= 0) {
            // Implementation of Confirmation dialog
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(
                    null,
                    "Are you sure you want to delete the selected account?",
                    "Warning",
                    dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                // Get the selected account object from table
                String selectedAccount = (String) tblAccounts.getValueAt(selectedRow, 0);

                // Delete from directory
                business.getStudentDirectory().deleteStudent(selectedAccount);

                // Refresh list/table
                populateTable();
            }
        } else {
            JOptionPane.showMessageDialog(
                    null,
                    "Please select an account from the list.",
                    "Warning",
                    JOptionPane.WARNING_MESSAGE
            );
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:

        if (!txtSearchBox.getText().isBlank()) {

            String accountNumber = txtSearchBox.getText();
            StudentDirectory studentDir = business.getStudentDirectory();
            StudentProfile profile = studentDir.findStudentByIdOrEmailIdOrName(accountNumber);

            if (profile != null) {

                ViewStudentJPanel panel = new ViewStudentJPanel(userProcessContainer, studentDir, profile, business);
                userProcessContainer.add("ViewAccountJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);

            } else {
                JOptionPane.showMessageDialog(null, "Account not found. Please check the account details like name or uni id and try again.",
                        "Warning", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please type the Name or  university ID  to view.", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnSearchActionPerformed


