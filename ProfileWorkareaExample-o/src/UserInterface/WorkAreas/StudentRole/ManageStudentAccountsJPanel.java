    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblAccounts.getModel();
        model.setRowCount(0);  // Clear existing rows

        for (StudentProfile a : business.getStudentDirectory().getStudentlist()) {
            Object[] row = new Object[5];
            row[0] = a.getPerson().getPersonId();                          // Stores Account object itself (to help with selection later)
            row[1] = a.getPerson().getName();
            row[2] = a.getPerson().getEmailId();
            row[3] = String.valueOf(a.getPerson().getContactInfo());
            row[4] = String.valueOf(a.getDepartment().getName());

            model.addRow(row);
        }
    }

