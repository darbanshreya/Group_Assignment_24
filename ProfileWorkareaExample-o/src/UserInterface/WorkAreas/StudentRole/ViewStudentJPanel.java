    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
    String studentName = txtName.getText();
    String deptName = txtDepart.getText();
    String studEmail = txtEmail.getText();
    String studContact = txtContact.getText();
     Department dept =   business.getDepartmentDictonary().getDepartmentByName(deptName);
    if (studentName.isBlank()  || deptName.isBlank() || dept == null) {
        JOptionPane.showMessageDialog(null, "All fields are mandatory. or given Department does not exist.");
        return;
    }
    
    profile.getPerson().setName(studentName);
    profile.getPerson().setEmailId(studEmail);
    profile.getPerson().setContactInfo(studContact);
    profile.setDepartment(dept);
    
    JOptionPane.showMessageDialog(null,
        "Account successfully updated.",
        "Warning",
        JOptionPane.WARNING_MESSAGE);

    setViewMode();
    }//GEN-LAST:event_btnSaveActionPerformed

