    private void refreshTextFields() {
    txtName.setText(profile.getPerson().getName());
    lblUniId.setText(profile.getPerson().getPersonId());
    txtDepart.setText(profile.getDepartment().getName());
    txtEmail.setText(profile.getPerson().getEmailId());
    txtContact.setText(profile.getPerson().getContactInfo());
    }

    private void setViewMode() {
    txtName.setEnabled(false);
    txtDepart.setEnabled(false);

    btnSave.setEnabled(false);
    btnUpdate.setEnabled(true);
    }
    
    private void setEditMode() {
    txtName.setEnabled(true);
    txtDepart.setEnabled(true);
    
    btnSave.setEnabled(true);
    btnUpdate.setEnabled(false);
    }
